function plotElasticityTensorsErrors
    C_corr = [0.6714 0.1926 -0.0002;0.1926 0.6714 -0.0002;-0.0002 -0.0002 0.1830];
    C(:,:,1) = [0.6693 0.1916 -0.0004;0.1916 0.6693 -0.0004;-0.0004 -0.0004 0.1819];
    C(:,:,2) = [0.6662 0.1897 -0.0010;0.1897 0.6662 -0.0010;-0.0010 -0.0010 0.1797];
    C(:,:,3) = [0.6569 0.1864 -0.0021;0.1864 0.6569 -0.0021;-0.0021 -0.0021 0.1783];
    C(:,:,4) = [0.6506 0.1811 -0.0036;0.1811 0.6506 -0.0036;-0.0036 -0.0036 0.1743];
    error_r = zeros(4,1);
    for i = 1:4
        error_r(i) = error_r(i)+abs(det(C(:,:,i)-C_corr))/abs(det(C_corr))*100;
    end
    plot([250 100 50 25],error_r);
    xlabel('Number of divisions per side');
    ylabel('Relative error (%)')
end